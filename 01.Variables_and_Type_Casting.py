# Define variables of different types
integer= 9
float = 19.6
string = "730"

# Print the values and their types
print( "Type:", type(integer))
print( "Type:", type(float))
print( "Type:", type(string))

# Convert the string to an integer
converted_integer = int(string)
print("Converted String to Integer:", converted_integer)

# Convert the float to an integer
converted_float = int(float)
print("Converted Float to Integer:", converted_float)